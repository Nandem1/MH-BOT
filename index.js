const { Client, LocalAuth } = require('whatsapp-web.js');
const qrcode = require('qrcode-terminal');
const axios = require('axios');
const FormData = require('form-data');
const fs = require('fs');
require('dotenv').config();

// Inicializar el cliente de WhatsApp con autenticaci√≥n local
const client = new Client({
  authStrategy: new LocalAuth(),
});

// Generar y mostrar el QR para iniciar sesi√≥n
client.on("qr", (qr) => {
  console.log("Escanea este c√≥digo QR con WhatsApp:");
  qrcode.generate(qr, { small: true });
});

// Confirmar que el bot est√° conectado
client.on("ready", () => {
  console.log("‚úÖ Bot de WhatsApp conectado y listo para recibir mensajes.");
});

// Escuchar mensajes en WhatsApp
client.on("message", async (message) => {
  console.log(`üì© Mensaje recibido de ${message.from}: ${message.body}`);

  if (message.body.toLowerCase() === "hola bot") {
    await client.sendMessage(
      message.from,
      "¬°Hola! Soy tu bot de facturas. ¬øC√≥mo puedo ayudarte?"
    );
  }
});

client.on("message", async (message) => {
  if (message.isGroupMsg) {
    // Solo mostrar mensajes de grupos
    console.log(
      `üì© Mensaje recibido en el grupo: ${message.from} - ${message.body}`
    );
  }
});

const GROUP_ID = process.env.GROUP_ID; // Reemplaza con el ID real del grupo

client.on("message", async (message) => {
  if (message.from === GROUP_ID) {
    // Solo en el grupo autorizado
    console.log(`üì© Mensaje recibido: ${message.body}`);

    // üìå CONSULTAR FACTURA: "BOT, tr√°eme el folio X"
    if (message.body.toLowerCase().startsWith("trae el folio")) {
      const folio = message.body.split(" ").pop(); // Extraer el n√∫mero de folio

      try {
        const response = await axios.get(
          `http://localhost:3000/api/facturas/${folio}`
        );
        const factura = response.data;

        if (factura) {
          await client.sendMessage(
            GROUP_ID,
            `üìÑ Factura encontrada para el folio ${folio}: ${factura.image_url}`
          );
        } else {
          await client.sendMessage(
            GROUP_ID,
            `‚ùå No se encontr√≥ una factura con el folio ${folio}.`
          );
        }
      } catch (error) {
        console.error("‚ùå Error al buscar factura:", error);
        await client.sendMessage(
          GROUP_ID,
          "‚ùå Error al buscar la factura. Int√©ntalo m√°s tarde."
        );
      }
    }

    // üìå SUBIR FACTURA: Detectar imagen con texto "[FOLIO]_[PROVEEDOR]"
    if (message.hasMedia && message.body.includes("_")) {
      const media = await message.downloadMedia();
      const [folio, proveedor] = message.body.split("_");

      if (!folio || !proveedor) {
        await client.sendMessage(
          GROUP_ID,
          "‚ùå Error: El formato debe ser [FOLIO]_[PROVEEDOR]."
        );
        return;
      }

      // Guardar la imagen temporalmente
      const fileName = `factura_${folio}.jpg`;
      const filePath = `./temp/${fileName}`;

      require("fs").writeFileSync(filePath, media.data, "base64");

    // Enviar imagen al backend
    const formData = new FormData();
    formData.append('factura', fs.createReadStream(filePath)); // ‚úÖ Corregido
    formData.append('folio', folio);
    formData.append('proveedor', proveedor);

      try {
        const response = await axios.post(
          "http://localhost:3000/api/uploadFactura",
          formData,
          {
            headers: { ...formData.getHeaders() },
          }
        );

        console.log("‚úÖ Respuesta recibida del backend:", response.status, response.data);

        await client.sendMessage(
          GROUP_ID,
          `‚úÖ Factura subida con √©xito`
        );
      } catch (error) {
        console.error("‚ùå Error al subir factura:", error);
        await client.sendMessage(
          GROUP_ID,
          "‚ùå Error al subir la factura. Int√©ntalo de nuevo."
        );
      }

      // Eliminar el archivo temporal
      require("fs").unlinkSync(filePath);
    }
  }
});
client.initialize();
